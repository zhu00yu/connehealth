CREATE TABLE DRUGS (
	DRUG_ID BIGINT GENERATED BY DEFAULT AS IDENTITY(START WITH 0) NOT NULL,
	CREATE_ON TIMESTAMP NOT NULL,
	CREATE_BY BIGINT NOT NULL,
	MODIFY_ON TIMESTAMP,
	MODIFY_BY BIGINT,
	STATUS INTEGER NOT NULL,
	DRUG_APPROVE_CODE NVARCHAR(100) NOT NULL,
	APPROVE_DATE TIMESTAMP,
	DRUG_STANDARD_CODE NVARCHAR(100) NOT NULL,
	TYPE NVARCHAR(100) NOT NULL,
	IS_OTC_DRUG BOOLEAN NOT NULL,
	IS_BASIC_DRUG BOOLEAN NOT NULL,
	IS_MEDICARE_DRUG BOOLEAN NOT NULL,
	MNEMONICS1 NVARCHAR(100) NOT NULL,
	MNEMONICS2 NVARCHAR(100) NOT NULL,
	GENERAL_NAME NVARCHAR(100) NOT NULL,
	BRAND_NAME NVARCHAR(100) NOT NULL,
	FORM NVARCHAR(100) NOT NULL,
	STRENGTH NVARCHAR(100) NOT NULL,
	STRENGTH_UNIT NVARCHAR(100) NOT NULL,
	PACKAGING NVARCHAR(255) NOT NULL,
	COMPANY NVARCHAR(100) NOT NULL,
	COMPANY_COUNTRY NVARCHAR(50) NOT NULL,
	COMPANY_ADDRESS NVARCHAR(255) NOT NULL,
	MANUFACTURER NVARCHAR(100) NOT NULL,
	MANUFACTURER_COUNTRY NVARCHAR(50) NOT NULL,
	MANUFACTURER_ADDRESS NVARCHAR(255) NOT NULL,
	DRUG_PHOTO_ID1 BIGINT,
	DRUG_PHOTO_ID2 BIGINT,
	DRUG_PHOTO_ID3 BIGINT,
	CONSTRAINT DRUGS_PK PRIMARY KEY (DRUG_ID)
);
CREATE INDEX SYS_IDX_DRUGS_PK_10382 ON DRUGS (DRUG_ID);