CREATE TABLE ALLERGENS (
	ALLERGEN_ID BIGINT GENERATED BY DEFAULT AS IDENTITY(START WITH 0) NOT NULL,
	TYPE NVARCHAR(100) NOT NULL,
	CODE NVARCHAR(500) NOT NULL,
	MNEMONICS NVARCHAR(500) NOT NULL,
	NAME NVARCHAR(500) NOT NULL,
	PARENT_ID BIGINT,
	CREATE_ON TIMESTAMP NOT NULL,
	CREATE_BY BIGINT NOT NULL,
	MODIFY_ON TIMESTAMP,
	MODIFY_BY BIGINT,
	STATUS INTEGER NOT NULL,
	CONSTRAINT ALLERGENS_PK PRIMARY KEY (ALLERGEN_ID)
);
CREATE INDEX SYS_IDX_ALLERGENS_PK_10441 ON ALLERGENS (ALLERGEN_ID);