CREATE TABLE ADVERSE_REACTIONS (
	REACTION_ID BIGINT GENERATED BY DEFAULT AS IDENTITY(START WITH 0) NOT NULL,
	TYPE NVARCHAR(100) NOT NULL,
	CODE NVARCHAR(500) NOT NULL,
	MNEMONICS NVARCHAR(500) NOT NULL,
	REACTION NVARCHAR(500) NOT NULL,
	"LEVEL" INTEGER,
	PARENT_ID BIGINT,
	CREATE_ON TIMESTAMP NOT NULL,
	CREATE_BY BIGINT NOT NULL,
	MODIFY_ON TIMESTAMP,
	MODIFY_BY BIGINT,
	STATUS INTEGER NOT NULL,
	CONSTRAINT ADVERSE_REACTIONS_PK PRIMARY KEY (REACTION_ID)
);
CREATE INDEX SYS_IDX_ADVERSE_REACTIONS_PK_10468 ON ADVERSE_REACTIONS (REACTION_ID);